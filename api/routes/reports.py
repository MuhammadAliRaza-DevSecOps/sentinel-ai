@router.get("/latest/summary")
async def get_latest_summary(db: AsyncSession = Depends(get_db)):
    """Sabse nayi scan ka quick summary."""
    result = await db.execute(
        select(ScanResult)
        .where(ScanResult.risk_level != "RUNNING")
        .order_by(desc(ScanResult.created_at))
        .limit(1)
    )
    scan = result.scalar_one_or_none()
    
    if not scan:
        return {"message": "Abhi tak koi scan nahi hua"}
    
    return {
        "scan_id":    scan.id,
        "risk_level": scan.risk_level,
        "counts": {
            "critical": scan.count_critical,
            "high":     scan.count_high,
            "medium":   scan.count_medium,
        },
        "created_at": scan.created_at.isoformat() if scan.created_at else None,
    }