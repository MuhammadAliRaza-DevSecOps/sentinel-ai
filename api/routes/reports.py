    scan = await db.get(ScanResult, scan_id)
    if not scan:
        raise HTTPException(status_code=404, detail="Scan nahi mila")

    findings = scan.findings_json or []
    
    risk_colors = {
        "PASS": "#00CC44", "FAIL": "#FF4B4B", "WARN": "#FFD700", "RUNNING": "#4488FF"
    }
    risk_color = risk_colors.get(scan.risk_level, "#888888")

